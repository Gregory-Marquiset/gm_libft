#include "../../includes/libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*new;

	new = malloc(sizeof(t_list));
	if (!new)
		return (new = 0);
	new->content = content;
	new->next = 0;
	return (new);
}
