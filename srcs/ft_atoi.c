#include "../includes/libft.h"

int	ft_atoi(const char *nptr)
{
	size_t	i;
	size_t	sign;
	size_t	rslt;

	i = 0;
	sign = 1;
	rslt = 0;
	while (nptr[i] == 32 || (nptr[i] >= 9 && nptr[i] <= 13))
		i++;
	if (nptr[i] == '+' || nptr[i] == '-')
	{
		if (nptr[i] == '-')
			sign = -sign;
		i++;
	}
	while (ft_isdigit(nptr[i]))
	{
		rslt = (rslt * 10) + nptr[i] - 48;
		i++;
	}
	return (rslt * sign);
}
